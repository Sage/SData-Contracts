        public string GetCountryCode(string country)
        {
            string temp = country.ToUpper();
            if (this.codes.ContainsValue(temp))
                return temp;
            if (this.codes.ContainsKey(temp))
                return (string)codes[temp];
            return "";
        }

        public string GetCountry(string countryCode)
        {
            string temp = countryCode.ToUpper();
            if (this.codes.ContainsKey(temp))
                return temp;
            if (this.codes.ContainsValue(temp))
                return (string)codes[temp];
            return "";
        }

        public string extractCountry(ref string Address)
        {
            foreach (string country in codes.Keys)
            {
                if (Address.IndexOf(country, StringComparison.InvariantCultureIgnoreCase)>=0)
                {
                    Address = Address.Remove(Address.IndexOf(country, StringComparison.InvariantCultureIgnoreCase), country.Length);
                    return country;
                }
            }
            return "";
        }
